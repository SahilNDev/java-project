    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/melokeys","root","root");
        Statement st = con.createStatement();
        String p1 = "", p2= "";
        for (char i: jPasswordField1.getPassword()) {
            p1 += i;
        }
        for (char i: jPasswordField2.getPassword()) {
            p2 += i;
        }
        ResultSet rs = st.executeQuery("select count(*) from user where username = '" + jTextField1.getText()+ "';");
        int r =0;
        while(rs.next()) {
            r = rs.getInt(1);
        }
        if(r == 1) {
            throw new SQLException("Username already used.");
        }
        else if(!p1.equals(p2)) {
            throw new SQLException("Password doesn't match.");
        }
        int l = Integer.parseInt(jTextField2.getText());
        String x = "insert into user values('" + jTextField3.getText() + "','" + jTextField1.getText() + "'," + l+",'"+ p1  +"');";
        st.executeUpdate(x);
        dispose();
        new Levels().setVisible(true);
        } catch(SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } catch (NumberFormatException f) {
            JOptionPane.showMessageDialog(this, "Age entered is not a number.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

